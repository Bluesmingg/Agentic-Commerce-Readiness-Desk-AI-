#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');
const { spawnSync } = require('node:child_process');

function parseArgs(argv) {
  const args = {
    url: '',
    client: '',
    maxProducts: 2,
    noHeadless: false,
    skipValidate: false,
  };

  for (let i = 2; i < argv.length; i += 1) {
    const arg = argv[i];
    const next = argv[i + 1];
    if (arg === '--url') {
      args.url = next || '';
      i += 1;
    } else if (arg === '--client') {
      args.client = next || '';
      i += 1;
    } else if (arg === '--max-products') {
      args.maxProducts = Math.max(1, Number.parseInt(next || '2', 10) || 2);
      i += 1;
    } else if (arg === '--no-headless') {
      args.noHeadless = true;
    } else if (arg === '--skip-validate') {
      args.skipValidate = true;
    } else if (arg === '--help' || arg === '-h') {
      printHelp();
      process.exit(0);
    }
  }

  return args;
}

function printHelp() {
  console.log(`Agentic Commerce store audit runner

Usage:
  npm run audit:store -- --url https://store.example --max-products 2
  npm run audit:store -- --url https://store.example --client store-mini-2026-05

Options:
  --url <url>             Public storefront URL.
  --client <name>         Optional local client folder name.
  --max-products <n>      Product pages to discover. Default: 2.
  --no-headless           Show browser while collecting.
  --skip-validate         Skip local syntax/JSON validation.
`);
}

function normalizeUrl(value) {
  try {
    const url = new URL(value);
    url.hash = '';
    return url.toString().replace(/\/$/, '');
  } catch {
    return '';
  }
}

function slugify(value) {
  return String(value)
    .replace(/^https?:\/\//i, '')
    .replace(/^www\./i, '')
    .replace(/[^a-z0-9]+/gi, '-')
    .replace(/^-|-$/g, '')
    .toLowerCase()
    .slice(0, 60) || 'store';
}

function todayIsoDate() {
  return new Date().toISOString().slice(0, 10);
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: process.cwd(),
    stdio: 'pipe',
    shell: false,
    encoding: 'utf8',
    ...options,
  });
  return {
    command: [command, ...args].join(' '),
    status: result.status,
    stdout: result.stdout || '',
    stderr: result.stderr || '',
    error: result.error,
  };
}

function assertOk(result) {
  if (result.status === 0) return;
  const output = [result.stdout, result.stderr, result.error?.message].filter(Boolean).join('\n').trim();
  throw new Error(`${result.command} failed with exit code ${result.status}\n${output}`);
}

function shortUrl(value) {
  try {
    const url = new URL(value);
    if (/\/checkouts?\//i.test(url.pathname)) {
      const checkoutRoot = url.pathname.toLowerCase().includes('/checkouts/') ? '/checkouts/...' : '/checkout/...';
      return `${url.origin}${checkoutRoot}`;
    }
    return `${url.origin}${url.pathname}`;
  } catch {
    return value || '-';
  }
}

function validationCommand() {
  if (process.platform === 'win32') {
    return { command: 'cmd.exe', args: ['/d', '/s', '/c', 'npm run validate'] };
  }
  return { command: 'npm', args: ['run', 'validate'] };
}

function issueRows(audit) {
  const issues = audit.issues || [];
  if (!issues.length) return '_No open issues were generated by the collector. Human review is still recommended._';
  return [
    '| ID | Severity | Category | Page | Issue | Effort |',
    '| --- | --- | --- | --- | --- | --- |',
    ...issues.map((issue) => `| ${issue.id} | ${issue.severity} | ${issue.category} | ${issue.page || '-'} | ${issue.title || '-'} | ${issue.effort || '-'} |`),
  ].join('\n');
}

function buildClientReport(audit) {
  const pages = audit.crawlResults?.pages || [];
  const checkout = audit.crawlResults?.checkoutHandoff || {};
  const checkoutPassed = Boolean(checkout.success ?? checkout.checkoutReached);
  const jsonLdPages = pages.filter((page) => (page.jsonld || page.jsonLd || []).length > 0).length;
  const warningPages = pages.filter((page) => (page.jsonldWarnings || page.warnings || []).length > 0).length;

  return `# Agentic Commerce Readiness Report

Store: ${audit.meta?.url || '-'}
Platform: ${audit.meta?.platform || '-'}
Generated: ${audit.meta?.generatedAt || '-'}

## Executive Summary

This check reviews whether the public storefront exposes product, policy, cart, and checkout signals that are clear enough for AI shopping agents and stable enough for human buyers.

## Collection Summary

- Pages crawled: ${pages.length}
- Pages with JSON-LD: ${jsonLdPages}
- Pages with warnings: ${warningPages}
- Open issues: ${(audit.issues || []).length}
- Checkout handoff: ${checkoutPassed ? 'Passed' : 'Needs review'}
- Checkout source: ${checkout.checkoutSource || '-'}
- Checkout URL: ${shortUrl(checkout.checkoutUrl || checkout.checkoutRedirectUrl || '')}
- Cart items observed: ${checkout.cartState?.items ?? '-'}

## Issue Backlog

${issueRows(audit)}

## Recommended Next Step

Review the generated findings manually, confirm business priority, then turn confirmed issues into a small frontend/schema remediation sprint.

## Safety Boundary

This audit used public storefront pages only. It does not require admin tokens, customer data, payment credentials, or payment submission.
`;
}

function buildCsv(audit) {
  const header = ['id', 'severity', 'category', 'page', 'title', 'businessImpact', 'fix', 'effort'];
  const rows = (audit.issues || []).map((issue) =>
    header.map((key) => `"${String(issue[key] || '').replace(/"/g, '""')}"`).join(',')
  );
  return [header.join(','), ...rows].join('\n');
}

function buildOpenClawTask(audit) {
  const issues = audit.issues || [];
  const issueText = issues.length
    ? issues.map((issue) => `- ${issue.id} [${issue.severity}] ${issue.title}: ${issue.fix}`).join('\n')
    : '- No collector-generated issues. Perform a human review of product, policy, cart, and checkout evidence.';

  return `Use OpenClaw to review this Agentic Commerce audit.

Store: ${audit.meta?.url || '-'}
Audit file: audit.generated.json

Tasks:
1. Review crawled pages and screenshot evidence.
2. Confirm whether each collector finding is a real merchant-facing issue.
3. Convert confirmed issues into implementation tasks with acceptance criteria.
4. Do not request tokens, admin access, customer data, or payment credentials.
5. Do not submit payment.

Findings:
${issueText}
`;
}

function appendClientIndex(clientId, storeUrl, clientDir, audit) {
  const indexPath = path.join('clients', '_index.md');
  const checkout = audit.crawlResults?.checkoutHandoff || {};
  const checkoutPassed = Boolean(checkout.success ?? checkout.checkoutReached) ? 'passed' : 'needs review';
  if (!fs.existsSync(indexPath)) {
    fs.writeFileSync(indexPath, '# Local Client Audit Index\n\nGenerated client evidence is local and ignored by Git.\n\n', 'utf8');
  }
  fs.appendFileSync(
    indexPath,
    `- ${new Date().toISOString()} | ${clientId} | ${storeUrl} | issues: ${(audit.issues || []).length} | pages: ${(audit.crawlResults?.pages || []).length} | checkout: ${checkoutPassed} | ${clientDir}\n`,
    'utf8'
  );
}

function buildRunRecord({ clientId, storeUrl, clientDir, audit, collectResult, validateResult, dashboardUrl }) {
  const checkout = audit.crawlResults?.checkoutHandoff || {};
  const checkoutPassed = Boolean(checkout.success ?? checkout.checkoutReached);
  return `# Audit Run Record

Client ID: ${clientId}
Store URL: ${storeUrl}
Generated: ${new Date().toISOString()}

## Outputs

- Audit JSON: ${path.join(clientDir, 'audit.generated.json')}
- Client report: ${path.join(clientDir, 'client-report.md')}
- Remediation CSV: ${path.join(clientDir, 'remediation.csv')}
- OpenClaw task: ${path.join(clientDir, 'openclaw-task.txt')}
- Dashboard URL: ${dashboardUrl}

## Result

- Pages crawled: ${(audit.crawlResults?.pages || []).length}
- Open issues: ${(audit.issues || []).length}
- Checkout handoff: ${checkoutPassed ? 'passed' : 'needs review'}
- Checkout source: ${checkout.checkoutSource || '-'}
- Checkout URL: ${shortUrl(checkout.checkoutUrl || checkout.checkoutRedirectUrl || '')}
- Warnings: ${(checkout.warnings || []).join('; ') || '-'}

## Commands

\`\`\`text
${collectResult.command}
${validateResult ? validateResult.command : '(validation skipped)'}
\`\`\`

## Collector Output

\`\`\`text
${collectResult.stdout.trim() || '(no stdout)'}
${collectResult.stderr.trim()}
\`\`\`
`;
}

function main() {
  const args = parseArgs(process.argv);
  const storeUrl = normalizeUrl(args.url);
  if (!storeUrl) {
    printHelp();
    process.exit(1);
  }

  const clientId = args.client ? slugify(args.client) : `${slugify(new URL(storeUrl).hostname)}-mini-${todayIsoDate()}`;
  const clientDir = path.join('clients', clientId);
  const auditPath = path.join(clientDir, 'audit.generated.json');
  ensureDir(clientDir);

  const collectArgs = [
    'collector-playwright.js',
    '--url',
    storeUrl,
    '--out',
    auditPath,
    '--max-products',
    String(args.maxProducts),
  ];
  if (args.noHeadless) collectArgs.push('--no-headless');

  console.log(`Running audit for ${storeUrl}`);
  console.log(`Client folder: ${clientDir}`);
  const collectResult = run(process.execPath, collectArgs);
  process.stdout.write(collectResult.stdout);
  process.stderr.write(collectResult.stderr);
  assertOk(collectResult);

  fs.copyFileSync(auditPath, 'audit.generated.json');
  const audit = JSON.parse(fs.readFileSync(auditPath, 'utf8'));
  fs.writeFileSync(path.join(clientDir, 'client-report.md'), buildClientReport(audit), 'utf8');
  fs.writeFileSync(path.join(clientDir, 'remediation.csv'), buildCsv(audit), 'utf8');
  fs.writeFileSync(path.join(clientDir, 'openclaw-task.txt'), buildOpenClawTask(audit), 'utf8');

  let validateResult = null;
  if (!args.skipValidate) {
    const validation = validationCommand();
    validateResult = run(validation.command, validation.args);
    process.stdout.write(validateResult.stdout);
    process.stderr.write(validateResult.stderr);
    assertOk(validateResult);
  }

  const dashboardUrl = `http://127.0.0.1:8099/?audit=generated&t=${encodeURIComponent(clientId)}`;
  fs.writeFileSync(
    path.join(clientDir, 'RUN_RECORD.md'),
    buildRunRecord({ clientId, storeUrl, clientDir, audit, collectResult, validateResult, dashboardUrl }),
    'utf8'
  );
  appendClientIndex(clientId, storeUrl, clientDir, audit);

  console.log('\nAudit complete.');
  console.log(`Client folder: ${clientDir}`);
  console.log(`Dashboard URL: ${dashboardUrl}`);
  console.log(`Issues: ${(audit.issues || []).length}`);
  console.log(`Pages: ${(audit.crawlResults?.pages || []).length}`);
}

main();
